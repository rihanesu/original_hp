                <h2>教室理念</h2>
            </div>
            <div class="philosophy-body col-md-10">
                <p>私は、音大卒業後からヤマハ音楽教室で７年<br class="br-sp" />ピアノ個人レッスンをしてきました。</p>
                <p class="philosophy-body-paragraph">この７年間で小さなお子様の指導にあたり<br class="br-sp" />教育の楽しさを感じました。</p>
                <p>初めてピアノを習うお子様は、何も知らない<br class="br-sp" />真っ白なキャンバスです。</p>
                <p class="philosophy-body-paragraph">そのお子様が少しずつ色をつけていく<br class="br-sp" />ように、音楽の楽しさを伝えていくことが<br class="br-sp" />大切だと感じました。</p>
                <p>ただピアノの前に座って弾かせているだけ<br class="br-sp" />では、すぐに飽きてしまいます。</p>
                <p class="philosophy-body-paragraph">いかに楽しくレッスンの時間を充実させるか<br class="br-sp" />に気を遣います。</p>
                <p>小さなお子様のレッスンにはソルフェージュといって、音楽の基礎を勉強します。</p>
                <p>ソルフェージュとは、リズム打ちをしたり、音読みをしたり読んだ音を音程をつけて<br class="br-sp" />歌ったりと</p>
                <p class="philosophy-body-paragraph">絶対音感の基礎になる訓練もします。</p>
                <p>お子様は、真っ白なキャンバスですから<br class="br-sp" />知らないことを知る楽しさに変えてグングン<br class="br-sp" />吸収していきます。</p>
                <p>その後小学生では、私がピアノを弾くと<br class="br-sp" />ノートにリズムも音も正確に書けるように<br class="br-sp" />なります。</p>
                <p>いわゆる聴音というものです。</p>
                <p class="philosophy-body-paragraph">お子様は、この聴音が大好きで、皆がもっとやりたいと言います。出来ることが楽しさに変わっていった証拠です。</p>
                <p>小学生も高学年になるとピアノの発表会では素晴らしい演奏ができるようになります。</p>
                <p class="philosophy-body-paragraph">学校の伴奏の代表にも選ばれて益々自信をつけていきます。</p>
                <p>卒業式の伴奏の代表に選ばれたり、学習<br class="br-sp" />発表会の伴奏に選ばれたりとピアノを習って<br class="br-sp" />いて本当に良かったと皆さん言っています。</p>
                <p>中学生になると合唱コンクールの伴奏に<br class="br-sp" />選ばれて市民会館大ホールでの演奏ができて<br class="br-sp" />やりがいもあり、音楽的表現が発揮できる</p>
                <p class="philosophy-body-paragraph">絶好の機会となります。</p>
                <p>大人の方もレッスンに多く通ってみえます。</p>
                <p class="philosophy-body-paragraph">主婦の方やお仕事をお持ちの方など、<br class="br-sp" />忙しい中、生きがいとやりがい、癒やしを<br class="br-sp" />求めてレッスンに通ってみえます。</p>
                <p>大人の方が発表会に出演されると会場<br class="br-sp" />からも、ひときわ大きな拍手に包まれ</p>
                <p class="philosophy-body-paragraph">皆様照れくさいような表情をしながらも、<br class="br-sp" />とても嬉しそうです。そして<br class="br-sp" />「また出演したい」と言われます。</p>
                <p>これからピアノを始めたい方も、ぜひこの<br class="br-sp" />音楽の扉を開いてみませんか？</p>
                <p class="philosophy-body-paragraph">必ず始めて良かったと思えることでしょう。</p>
                <p>１人レッスンの良さは、一人一人と真剣に<br class="br-sp" />向き合えることではないでしょうか？</p>
                <p>グループレッスンですと、どうしても年間<br class="br-sp" />カリキュラムに添って進めなくては<br class="br-sp" />いけません。</p>
                <p class="philosophy-body-paragraph">出来ても出来なくても、分かっても分かって<br class="br-sp" />いなくてもどんどん進んでいきます。</p>
                <p>個人レッスンは、出来ていることをしっかり<br class="br-sp" />と確認しながら進んでいきます。</p>
                <p class="philosophy-body-paragraph">途中で分からなくなった時は、少し戻って<br class="br-sp" />復習をしながら進んでいきます。</p>
                <p>ピアノ個人レッスンですと、お母様から子育て<br class="br-sp" />の相談をされることも多いです。</p>
                <p>私も３人の子供の母親です。<br class="br-sp" />子育てで悩んだりして子供を育ててきました<br class="br-sp" />ので多少なりの子育て経験からお母様に</p>
                <p class="philosophy-body-paragraph">アドバイスをすることもあります。</p>
                <p>話をしていると、子育てで悩んでいる<br class="br-sp" />お母様が、ふっと顔が明るくなり帰られる<br class="br-sp" />頃には素敵な笑顔で帰っていかれます。</p>
                <p>大人の方で<br class="br-sp" />「性格的に他の方と話すのが苦手です」<br class="br-sp" />と言われる方も見えて</p>
                <p class="philosophy-body-paragraph">私が「私もそうですよ」と話していると<br class="br-sp" />会話が弾み、その後レッスンもどんどん<br class="br-sp" />進んでいきます。</p>
                <p>ピアノレッスンは、ただピアノを弾くだけでは<br class="br-sp" />なく、相手への思いやりや信頼関係がないと<br class="br-sp" />前に進めないなと感じます。</p>
                <p class="philosophy-body-paragraph">人と人との深いつながりがあってこそ、<br class="br-sp" />レッスンもはかどります。</p>
                <p>２６年の実績がありますのでどこが弱点か、<br class="br-sp" />どこを伸ばしていけば良いか、こういう<br class="br-sp" />話し方の方が分かりやすいのか</p>
                <p>今までの経験を基に進めていきますので、<br class="br-sp" />お任せして頂きたいです。</p>
                <p class="philosophy-body-paragraph">ピアノを始めるにあたっては、心配なことも<br class="br-sp" />多いでしょう。</p>
                <p>でも初めは誰もピアノは弾けません。<br class="br-sp" />出来なくて当たり前です。</p>
                <p class="philosophy-body-paragraph">しかし他の人と比べることは良く<br class="br-sp" />ありません。</p>
                <p>半年後、一年後の自分と比べて下さい。<br class="br-sp" />必ず成長が確認出来るはずです。</p>
                <p class="philosophy-body-paragraph">そして発表会で自分の演奏を披露出来たら<br class="br-sp" />それはとても素敵なことです。</p>
                <p>一人だけで弾いているのも勿論素敵な<br class="br-sp" />ことです。</p>
                <p class="philosophy-body-paragraph">しかし発表会は、演奏と同時に自分というものが表現できる絶好の機会です。</p>
                <p>人前で弾くことがどれだけ人を成長させるのでしょうか。</p>
                <p class="philosophy-body-paragraph">発表会１回出演するのは、100回分の<br class="br-sp" />レッスンに匹敵すると言われます。</p>
                <p>生徒がこの曲を弾きたいと言って難しい曲を<br class="br-sp" />持ってきた時、私は心の中でちょっと<br class="br-sp" />無理かな？と思っていても</p>
                <p class="philosophy-body-paragraph">必ずやり遂げます。素晴らしく上手に弾ける<br class="br-sp" />ようになります。</p>
                <p>どこからこんなパワーが出てくるのかと<br class="br-sp" />思います。</p>
                <p class="philosophy-body-paragraph">弾きたい気持ちがどんなことでも乗り越える<br class="br-sp" />強い気持ちに変わるからでしょう。</p>
                <p>どんなことも出来ることに変えてしまう<br class="br-sp" />パワーがある発表会だからです。</p>
                <p class="philosophy-body-paragraph">本当に発表会は魔法ですね。</p>
                <p>発表会で、生徒の曲を一曲一曲聴いていると<br class="br-sp" />その人の人生が見えてきます。</p>
                <p class="philosophy-body-paragraph">この曲にかける想いというのか・・・<br class="br-sp" />そういうものが伝わってきます。</p>
